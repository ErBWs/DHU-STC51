#include "ds18b20.h"


/*******************************************************************************
* 函 数 名         : ds18b20_reset
* 函数功能		   : 复位DS18B20  
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/
void ds18b20_reset(void)
{
    DS18B20_PORT = 0;    //拉低DQ
    SoftDelay_us(750);    //拉低750us
    DS18B20_PORT = 1;    //DQ=1
    SoftDelay_us(20);    //20US
}

/*******************************************************************************
* 函 数 名         : ds18b20_check
* 函数功能		   : 检测DS18B20是否存在
* 输    入         : 无
* 输    出         : 1:未检测到DS18B20的存在，0:存在
*******************************************************************************/
uint8_t ds18b20_check(void)
{
    uint8_t time_temp = 0;

    while (DS18B20_PORT && time_temp < 20)    //等待DQ为低电平
    {
        time_temp++;
        SoftDelay_us(10);
    }
    if (time_temp >= 20)return 1;    //如果超时则强制返回1
    else time_temp = 0;
    while ((!DS18B20_PORT) && time_temp < 20)    //等待DQ为高电平
    {
        time_temp++;
        SoftDelay_us(10);
    }
    if (time_temp >= 20)return 1;    //如果超时则强制返回1
    return 0;
}

/*******************************************************************************
* 函 数 名         : ds18b20_read_bit
* 函数功能		   : 从DS18B20读取一个位
* 输    入         : 无
* 输    出         : 1/0
*******************************************************************************/
uint8_t ds18b20_read_bit(void)
{
    uint8_t dat = 0;

    DS18B20_PORT = 0;
    DELAY;
    DS18B20_PORT = 1;
    DELAY;
    if (DS18B20_PORT)dat = 1;    //如果总线上为1则数据dat为1，否则为0
    else dat = 0;
    SoftDelay_us(5);
    return dat;
}

/*******************************************************************************
* 函 数 名         : ds18b20_read_byte
* 函数功能		   : 从DS18B20读取一个字节
* 输    入         : 无
* 输    出         : 一个字节数据
*******************************************************************************/
uint8_t ds18b20_read_byte(void)
{
    uint8_t i = 0;
    uint8_t dat = 0;
    uint8_t temp = 0;

    for (i = 0; i < 8; i++)//循环8次，每次读取一位，且先读低位再读高位
    {
        temp = ds18b20_read_bit();
        dat = (temp << 7) | (dat >> 1);
    }
    return dat;
}

/*******************************************************************************
* 函 数 名         : ds18b20_write_byte
* 函数功能		   : 写一个字节到DS18B20
* 输    入         : dat：要写入的字节
* 输    出         : 无
*******************************************************************************/
void ds18b20_write_byte(uint8_t dat)
{
    uint8_t i = 0;
    uint8_t temp = 0;

    for (i = 0; i < 8; i++)//循环8次，每次写一位，且先写低位再写高位
    {
        temp = dat & 0x01;//选择低位准备写入
        dat >>= 1;//将次高位移到低位
        if (temp)
        {
            DS18B20_PORT = 0;
            DELAY;
            DS18B20_PORT = 1;
            SoftDelay_us(6);
        } else
        {
            DS18B20_PORT = 0;
            SoftDelay_us(6);
            DS18B20_PORT = 1;
            DELAY;
        }
    }
}

/*******************************************************************************
* 函 数 名         : ds18b20_start
* 函数功能		   : 开始温度转换
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/
void ds18b20_start(void)
{
    ds18b20_reset();//复位
    ds18b20_check();//检查DS18B20
    ds18b20_write_byte(0xcc);//SKIP ROM
    ds18b20_write_byte(0x44);//转换命令	
}

/*******************************************************************************
* 函 数 名         : ds18b20_init
* 函数功能		   : 初始化DS18B20的IO口 DQ 同时检测DS的存在
* 输    入         : 无
* 输    出         : 1:不存在，0:存在
*******************************************************************************/
uint8_t ds18b20_init(void)
{
    ds18b20_reset();
    return ds18b20_check();
}

/*******************************************************************************
* 函 数 名         : ds18b20_read_temperture
* 函数功能		   : 从ds18b20得到温度值
* 输    入         : 无
* 输    出         : 温度数据
*******************************************************************************/
float ds18b20_read_temperture(void)
{
    float temp;
    uint8_t dath = 0;
    uint8_t datl = 0;
    uint16_t value = 0;

    ds18b20_start();//开始转换
    ds18b20_reset();//复位
    ds18b20_check();
    ds18b20_write_byte(0xcc);//SKIP ROM
    ds18b20_write_byte(0xbe);//读存储器

    datl = ds18b20_read_byte();//低字节
    dath = ds18b20_read_byte();//高字节
    value = (dath << 8) + datl;//合并为16位数据

    if ((value & 0xf800) == 0xf800)//判断符号位，负温度
    {
        value = (~value) + 1; //数据取反再加1
        temp = value * (-0.0625);//乘以精度
    } else //正温度
    {
        temp = value * 0.0625;
    }
    return temp;
}
